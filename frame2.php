<h1>Features</h1>
	<ul>
		<li>Users will be able to view match reports.</li>
		<li>Tournaments schedules will be easily available to users.</li>
		<li>Players and tournaments organizers can easily create and run their tournaments</li>
	</ul>

	<h1>Modules</h1>
	<ol>
		<li><b>Landing Page</b> - E-sports News, New tournaments, Live scores</li>
		<li><b>Login/Sign Up Page</b> - Adding new user / signing in</li>
		<li><b>Event/Tournament Details</b> - A detailed description of an event including teams participating, sponsors, event dates and prize pool.</li>
		<li><b>Create Tournaments/bracket</b> - Host a Tournament Online or create a bracket for Local Matches</li>
		<li><b>Join a Clan/Team</b> - Manage your Team and participate</li>

	</ol>
